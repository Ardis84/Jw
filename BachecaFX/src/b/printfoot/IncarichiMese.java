package b.printfoot;

public class IncarichiMese {
	
	String id;
	String idincaricato;
	String idincarico;
	String data;
	String mese;
	String anno;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdincaricato() {
		return idincaricato;
	}
	public void setIdincaricato(String idincaricato) {
		this.idincaricato = idincaricato;
	}
	public String getIdincarico() {
		return idincarico;
	}
	public void setIdincarico(String idincarico) {
		this.idincarico = idincarico;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getMese() {
		return mese;
	}
	public void setMese(String mese) {
		this.mese = mese;
	}
	public String getAnno() {
		return anno;
	}
	public void setAnno(String anno) {
		this.anno = anno;
	}
	
	

}
